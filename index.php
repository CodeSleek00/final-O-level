        // Animation for cards when they come into view
        const cards = document.querySelectorAll('.card, .service-card, .practice-card, .review-section-card');
        
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateY(0)';
                }
            });
        }, { threshold: 0.1 });

        cards.forEach((card, index) => {
            card.style.opacity = '0';
            card.style.transform = 'translateY(20px)';
            card.style.transition = `all 0.5s ease ${index * 0.1}s`;
            observer.observe(card);
        });

        // Animated counter for statistics
        document.addEventListener("DOMContentLoaded", () => {
            const counters = document.querySelectorAll(".review-section-stat-number");
            let counted = false;

            const animateCount = (counter) => {
                const target = +counter.getAttribute("data-count");
                let count = 0;
                const speed = target > 100 ? 10 : 50;
                const step = target / 100;

                const update = () => {
                    if (count < target) {
                        count += step;
                        counter.textContent = target >= 100 ? Math.floor(count) : count.toFixed(1);
                        setTimeout(update, speed);
                    } else {
                        counter.textContent = target >= 100 ? target + "+" : target;
                    }
                };

                update();
            };

            const statObserver = new IntersectionObserver((entries, observer) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting && !counted) {
                        counters.forEach(counter => animateCount(counter));
                        counted = true;
                        observer.disconnect();
                    }
                });
            }, {
                threshold: 0.5
            });

            const statSection = document.querySelector(".review-section-stats");
            if (statSection) statObserver.observe(statSection);
        });
    </script>

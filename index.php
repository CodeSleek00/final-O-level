  document.addEventListener("DOMContentLoaded", () => {
    const counters = document.querySelectorAll(".review-section-stat-number");
    let counted = false;

    const animateCount = (counter) => {
      const target = +counter.getAttribute("data-count");
      let count = 0;
      const speed = target > 100 ? 10 : 50;
      const step = target / 100;

      const update = () => {
        if (count < target) {
          count += step;
          counter.textContent = target >= 100 ? Math.floor(count) : count.toFixed(1);
          setTimeout(update, speed);
        } else {
          counter.textContent = target >= 100 ? target + "+" : target;
        }
      };

      update();
    };

    const observer = new IntersectionObserver((entries, observer) => {
      entries.forEach(entry => {
        if (entry.isIntersecting && !counted) {
          counters.forEach(counter => animateCount(counter));
          counted = true;
          observer.disconnect(); // Stop observing after counting once
        }
      });
    }, {
      threshold: 0.5
    });

    const statSection = document.querySelector(".review-section-stats");
    if (statSection) observer.observe(statSection);
  });

  </script>
